public class Bucles {
    public static void main(String[] args) {

        String [] nombres = {"Carlos", "Manuelos", "Marias", "Pedro"};
        String [] apellidos = {"Perez", "Mejias", "Ruiz", "Arez"};

        for (int i = 0; i < nombres.length; i++) {
            System.out.println(nombres[i] + " " + apellidos[i]);
        }
    }
}
